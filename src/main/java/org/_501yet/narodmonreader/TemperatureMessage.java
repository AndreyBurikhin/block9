package org._501yet.narodmonreader;

public class TemperatureMessage {

	private double value;
	
	private long time;

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}

	public long getTime() {
		return time;
	}

	public void setTime(long time) {
		this.time = time;
	}
	
}
